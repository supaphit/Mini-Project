    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String id = txtStudentID.getText();
        String name = txtStudentName.getText();
        String surname = txtSurname.getText();
        String midterm = txtMidtrem.getText();
        String last = txtFinal.getText();
        if (id.length() > 0 && name.length() > 0 && surname.length() > 0 && midterm.length() > 0 && last.length() > 0) {
            int midtermInt = Integer.valueOf(midterm);
            int lastInt = Integer.valueOf(last);
            sum = lastInt + midtermInt;
            txtTotal.setText("" + sum);
            if (min > sum) {
                min = sum;
            }
            if (max < sum) {
                max = sum;
            }
            Average = Average + sum;
            String Grade;
            if (sum >= 80) {
                Grade = "A";
                A++;
            } else if (sum >= 70) {
                Grade = "B";
                B++;
            } else if (sum >= 60) {
                Grade = "C";
                C++;
            } else if (sum >= 50) {
                Grade = "D";
                D++;
            } else {
                Grade = "F";
                F++;
            }
            txtGrade.setText(Grade);
            i++;
            String totalS = String.valueOf(sum);
            String rowData[] = {id, name, surname, midterm, last, totalS, Grade};
            model.addRow(rowData);
            txtStudentName.setText("");
            txtStudentID.setText("");
            txtSurname.setText("");
            txtMidtrem.setText("");
            txtFinal.setText("");
        } else {
            JOptionPane.showMessageDialog(rootPane, "กรุณาป้อนข้อมูลให้ครบ");
        }
    }//GEN-LAST:event_btnAddActionPerformed

