    private void btnSummaryGradeAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_btnSummaryGradeAncestorAdded
        // TODO add your handling code here:
         try {
            DecimalFormat df = new DecimalFormat("#.##");
            int a = 0, b = 0, c = 0, d = 0, e = 0;
            float min = 999, max = 0;
            float sum_score = 0, avg = 0;
            int rowCount = tblData.getModel().getRowCount();
            for (int i = 0; i < rowCount; i++) {
                String grade =(String) tblData.getValueAt(i, 6);
                switch (grade) {
                    case "A":
                        a++;
                        break;
                    case "B":
                        b++;
                        break;
                    case "C":
                        c++;
                        break;
                    case "D":
                        d++;
                        break;
                    case "F":
                        e++;
                        break;
                    default:
                        break;
                       
                }
                String total =(String) tblData.getValueAt(i, 5);
                float totals = Float.valueOf(total);
                if (min > totals) {
                    min = totals;
                    
                }
                if (max < totals) {
                    max = totals;
                    
                }
                sum_score += totals;
                
            }
            avg = Float.parseFloat(df.format(sum_score / rowCount));
            lblGradeA.setText(".........." + String.valueOf(a) + "..........");
            lblGradeB.setText(".........." + String.valueOf(b) + "..........");
            lblGradeC.setText(".........." + String.valueOf(c) + "..........");
            lblGradeD.setText(".........." + String.valueOf(d) + "..........");
            lblGradeF.setText(".........." + String.valueOf(e) + "..........");
            lblTotal.setText(".........." + String.valueOf(a + b + c + d + e) + "..........");
            lblMax.setText(".........." + String.valueOf(max) + "..........");
            lblMin.setText(".........." + String.valueOf(min) + "..........");
            lblAverage.setText(".........." + String.valueOf(avg) + "..........");
        } catch (Exception e) {
            clearGradeScore();
        }
        
    }//GEN-LAST:event_btnSummaryGradeAncestorAdded

